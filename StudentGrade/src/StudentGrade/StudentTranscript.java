    private void jButtonResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResultActionPerformed
        
        double[] R = new double[10];
        R[0] = Double.parseDouble(jTextMath.getText());
        R[1] = Double.parseDouble(jTextSci.getText());
        R[2] = Double.parseDouble(jTextEng.getText());
        R[3] = Double.parseDouble(jTextFil.getText());
        R[4] = Double.parseDouble(jTextHis.getText());
        R[5] = Double.parseDouble(jTextMapeh.getText());
        R[6] = Double.parseDouble(jTextGmrc.getText());
        
        R[7] = (R[0] + R[1] + R[2] + R[3] + R[4] + R[5] + R[6]) / 7;
        
        String average = String.format("%.0f", R[7]);
        jTextAverage.setText(average);
        
        
        String lrn = jTextLrn.getText();
        String name = jTextName.getText();
        String grade = jComboGrade.getSelectedItem().toString();
        String section = jComboSection.getSelectedItem().toString();
        String math = jTextMath.getText();
        String sci = jTextSci.getText();
        String eng = jTextEng.getText();
        String fil = jTextFil.getText();
        String his = jTextHis.getText();
        String mapeh = jTextMapeh.getText();
        String gmrc = jTextGmrc.getText();
        String ave = jTextAverage.getText();
        
        String path = "C:\\Users\\Acer-PC\\Desktop\\trytextfile.txt";
        String content = "\n" + lrn + "                 " + name + "              " + grade + "                " + section + "            " + ave;
        
        try{
            Files.write(Paths.get(path), content.getBytes(), StandardOpenOption.APPEND);
        }
        catch(IOException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButtonResultActionPerformed

