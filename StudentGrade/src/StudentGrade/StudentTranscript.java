    private void Update(){
        int c;
        
        try {
            Class.forName("java.sql.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/transcript","root","");
            pst = conn.prepareStatement("select * from transcriptdata");
            
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
            Df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                
                for(int a=1; a<=c; a++){
                    v2.add(rs.getString("num"));
                    v2.add(rs.getString("lrn"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("grade"));
                    v2.add(rs.getString("section"));
                    v2.add(rs.getString("math"));
                    v2.add(rs.getString("sci"));
                    v2.add(rs.getString("eng"));
                    v2.add(rs.getString("fil"));
                    v2.add(rs.getString("his"));
                    v2.add(rs.getString("mapeh"));
                    v2.add(rs.getString("gmrc"));
                    v2.add(rs.getString("ave"));
                    
                }
                
                Df.addRow(v2);
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentTranscript.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StudentTranscript.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
